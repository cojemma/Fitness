package com.fitness.sdk.domain.model

/**
 * Enum representing major muscle groups in the body.
 * Used to categorize exercises by the primary muscles they target.
 */
enum class MuscleGroup {
    // Upper Body - Push
    CHEST,
    SHOULDERS,
    TRICEPS,

    // Upper Body - Pull
    BACK,
    BICEPS,
    FOREARMS,

    // Lower Body
    QUADRICEPS,
    HAMSTRINGS,
    GLUTES,
    CALVES,

    // Core
    CORE,

    // Full Body
    FULL_BODY
}
