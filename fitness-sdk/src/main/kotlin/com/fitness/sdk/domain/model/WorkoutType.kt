package com.fitness.sdk.domain.model

/**
 * Enum representing different types of workouts.
 */
enum class WorkoutType {
    /** Strength training workouts (weight lifting, resistance training) */
    STRENGTH,

    /** Cardiovascular workouts (running, cycling, swimming) */
    CARDIO,

    /** Flexibility workouts (yoga, stretching) */
    FLEXIBILITY,

    /** High-Intensity Interval Training */
    HIIT,

    /** Sports activities (basketball, tennis, etc.) */
    SPORTS,

    /** Custom or other workout types */
    OTHER
}
